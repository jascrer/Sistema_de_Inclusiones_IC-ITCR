public class Curso {
	public int Id_Curso;
    public String Cod_Curso, Txt_Curso;
	public int getId_Curso() {
		return Id_Curso;
	}
	public void setId_Curso(int id_Curso) {
		Id_Curso = id_Curso;
	}
	public String getCod_Curso() {
		return Cod_Curso;
	}
	public void setCod_Curso(String cod_Curso) {
		Cod_Curso = cod_Curso;
	}
	public String getTxt_Curso() {
		return Txt_Curso;
	}
	public void setTxt_Curso(String txt_Curso) {
		Txt_Curso = txt_Curso;
	}
}
